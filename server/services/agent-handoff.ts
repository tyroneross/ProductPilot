/**
 * agent-handoff — Phase 5 (2026-05-02).
 *
 * `generateHandoff(spec, productState) → string` returns the full Markdown
 * `agent-handoff.md` content a coding agent (Claude Code, Cursor, Codex)
 * pastes directly. The handoff is the artifact the entire adaptive-intake
 * flow exists to deliver.
 *
 * Section ordering is INTENTIONALLY prompt-cache-friendly:
 *
 *   1. Build objective + persona + non-goals     ← stable identity
 *   2. Stance + tradeoff weights                 ← stable strategic frame
 *   3. ADRs + data model + API contracts          ← architecture (changes slowly)
 *   4. Platform-specific test scaffolding         ← per-target build instructions
 *   5. Ask-before policy                          ← guardrails (verbatim)
 *   6. Specific Needs / Features / UX flows       ← dynamic specifics last
 *   7. Assumptions + risks                        ← dynamic
 *
 * Anthropic prompt caching keys on identical leading prefix — putting
 * identity content first means a re-paste with edits at the bottom keeps
 * the cache warm.
 *
 * Security:
 *   - Caller is expected to scrub PII / secrets BEFORE calling this function.
 *     The route layer runs scrubSecretsDeep on the spec + productState.
 *   - DataPoints with pii=true are surfaced by NAME + handlingNote only;
 *     their contents are never echoed.
 *   - Section text is regex-scrubbed for secret-shaped strings as a defense
 *     in depth (sk-/ghp_/AKIA/long base64).
 */

