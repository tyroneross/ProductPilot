function truncate(s: string, n: number): string {
  if (typeof s !== "string") return "";
  return s.length <= n ? s : `${s.slice(0, n - 1)}…`;
}

/**
 * Defense-in-depth: scrub anything that looks like a secret. The route layer
 * runs scrubSecretsDeep on the spec + productState before invoking us, but a
 * stray match here means the original scrubber missed something — better to
 * redact than leak.
 */
const SECRET_PATTERNS: RegExp[] = [
  /\bsk-[A-Za-z0-9_-]{16,}\b/g,
  /\bghp_[A-Za-z0-9]{24,}\b/g,
  /\bgho_[A-Za-z0-9]{24,}\b/g,
  /\bAKIA[0-9A-Z]{12,20}\b/g,
  /\baws_secret_access_key\s*[=:]\s*[A-Za-z0-9/+]{30,}\b/gi,
  /\b[A-Za-z0-9_-]{40,}\b\.[A-Za-z0-9_-]{8,}\.[A-Za-z0-9_-]{8,}\b/g, // jwt-shaped
];

function scrubSecretShapedStrings(s: string): string {
  let out = s;
  for (const re of SECRET_PATTERNS) {
    out = out.replace(re, "[REDACTED]");
  }
  return out;
}

